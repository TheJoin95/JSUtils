/* 
* parseXML
* try to parse a string in text/xml
* 
* @function parseXML
* @param string
* @memberof Utils
* @public
* @error if parseFromString return an xml error "<parsererror>" => console.error the invalid xml
* @returns xml if parseFromString of data return true | string | undefined
**/
Utils.prototype.parseXML = function( data ) {
	var xml;
	if ( !data || typeof data !== "string" ) {
		return null;
	}

	// Support: IE9
	try {
		// ParseFromString method 
		// http://help.dottoro.com/ljcilrao.php
		xml = ( new window.DOMParser() ).parseFromString( data, "text/xml" );
	} catch ( e ) {
		xml = undefined;
	}

	if ( !xml || xml.getElementsByTagName( "parsererror" ).length ) {
		console.error( "Invalid XML: " + data );
	}
	return xml;
};
